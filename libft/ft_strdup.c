#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		i;

	i = -1;
	while (s1[++i] != '\0')
		;
	if (!(s2 = (char *)malloc(i + 1)))
		return (0);
	i = -1;
	while (s1[++i] != '\0')
		s2[i] = s1[i];
	s2[i] = '\0';
	return (s2);
}
