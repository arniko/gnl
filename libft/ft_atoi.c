int		ft_atoi(const char *s)
{
	int value;
	int sign;
	int i;

	value = 0;
	sign = 1;
	i = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t' ||
			s[i] == '\f' || s[i] == '\r' || s[i] == '\v' || s[i] == '+')
		i++;
	if (s[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (s[i] >= 48 && s[i] <= 57)
	{
		value *= 10;
		value += ((int)s[i] - '0');
		s++;
	}
	return (value * sign);
}
